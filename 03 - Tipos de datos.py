# Funcion type()
numero = 5600
texto = "Hello, World"
pi = 3.14

print(type(numero)) # int
print(type(texto)) # str
print(type(pi)) # float


# Conversion de tipos

# str() convierte a string
numero_string = str(1234)
print(type(numero_string))

# int() convierte a entero
numero_int = int("25")
print(type(numero_int))

numero_float = int(3.14)
print(type(numero_float))

