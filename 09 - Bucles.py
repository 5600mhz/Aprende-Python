# Bucle While

numero = 1

# Mientras numero <= 10
while numero <= 10:
    print(f"Numero: {numero}")
    # Aumentar en 1
    numero = numero + 1


# Bucle For
texto = "Hello, World!"

for i in texto:
    print(i)



# Otra forma de aumentar en 1
numero += 1
