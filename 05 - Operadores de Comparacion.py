# Operadores de Comparacion

num1 = 5
num2 = 6

# Igual que (==)
# False, son diferentes
print(num1 == num2)

# Distinto que (!=)
# True, son diferentes
print(num1 != num2)

# Mayor que (>)
# False, num1 NO es mayor
print(num1 > num2)

# Menor que (<)
# True, num1 es menor
print(num1 < num2)

# Mayor o igual que (>=)
# False, num1 NO es mayor o igual
print(num1 >= num2)

# Menor o igual que (<=)
# True, num1 es menor o igual
print(num1 <= num2)


