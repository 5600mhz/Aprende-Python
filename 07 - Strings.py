# Metodos

# .upper()
# Convierte el string a
# mayusculas
print("hola".upper()) # HOLA

# .lower()
# Convierte el string a
# minisuculas
print("HOLA".lower()) # hola

# .capitalize()
# Convierte la primera letra
# en mayuscula
print("hello world".capitalize())
