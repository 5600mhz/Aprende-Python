# Operadores Logicos

condicion1 = True
condicion2 = False

# OR devuelve True si una de
# las condiciones evaluadas es
# True
print(condicion1 or condicion2)

# AND devuelve True si AMBAS
# condiciones son True
print(condicion1 and condicion2)

# NOT devuelve True si la
# condicion evaluada es False
print(not condicion2)


# Se pueden combinar con
# parentesis
print(not (True and False))
